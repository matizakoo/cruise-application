package com.zak.cruise.entity;

public class Ship {
    private Integer uniqueId;
    private Integer capacity;
    private Integer numberOfPorts;
    private String uniqueName;
    private Staff staff;

    public Ship(Integer uniqueId, Integer capacity, Integer numberOfPorts, String uniqueName, Staff staff) {
        this.uniqueId = uniqueId;
        this.capacity = capacity;
        this.numberOfPorts = numberOfPorts;
        this.uniqueName = uniqueName;
        this.staff = staff;
    }

    public Integer getUniqueId() {
        return uniqueId;
    }

    public void setUniqueId(Integer uniqueId) {
        this.uniqueId = uniqueId;
    }

    public Integer getCapacity() {
        return capacity;
    }

    public void setCapacity(Integer capacity) {
        this.capacity = capacity;
    }

    public Integer getNumberOfPorts() {
        return numberOfPorts;
    }

    public void setNumberOfPorts(Integer numberOfPorts) {
        this.numberOfPorts = numberOfPorts;
    }

    public String getUniqueName() {
        return uniqueName;
    }

    public void setUniqueName(String uniqueName) {
        this.uniqueName = uniqueName;
    }

    public Staff getStaff() {
        return staff;
    }

    public void setStaff(Staff staff) {
        this.staff = staff;
    }
}
